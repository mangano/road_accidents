### Utilities ###


def write_pickle(filename, obj):
    outfile = open(filename,'wb')
    pickle.dump(obj, outfile)
    outfile.close()

def read_pickle(filename):
    infile = open(filename,'rb')
    object = pickle.load(infile)
    infile.close()
    return object




